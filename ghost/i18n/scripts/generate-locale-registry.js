/* global process */
/**
 * SPIKE codegen — generates a static ESM per-namespace locale registry.
 *
 * The CJS build loads locales via a dynamic `require(`../locales/${locale}/${ns}.json`)`.
 * Bundlers using Rolldown can't resolve that (no `dynamicRequireTargets`), so browser
 * bundles silently drop locales. This script emits a file with STATIC `import`s of every
 * locale JSON, keyed by `${locale}/${ns}` — a shape any ESM bundler tree-shakes and resolves
 * with zero dynamic-require magic.
 *
 * Output: ghost/i18n/lib/locale-registry.generated.mjs
 */
const fs = require('fs');
const path = require('path');

const I18N_ROOT = path.join(__dirname, '..');
const LOCALES_DIR = path.join(I18N_ROOT, 'locales');
const LOCALE_DATA = require('../lib/locale-data.json');
const OUT = path.join(I18N_ROOT, 'lib', 'locale-registry.generated.mjs');

const NAMESPACES = ['ghost', 'portal', 'signup-form', 'comments', 'search'];

const locales = LOCALE_DATA.map(l => l.code);

function identFor(locale, ns) {
    return `_${locale}_${ns}`.replace(/[^a-zA-Z0-9_]/g, '_');
}

const importLines = [];
const entries = [];

for (const locale of locales) {
    for (const ns of NAMESPACES) {
        const abs = path.join(LOCALES_DIR, locale, `${ns}.json`);
        if (!fs.existsSync(abs)) {
            // Not every locale ships every namespace; only reference files that exist.
            continue;
        }
        const ident = identFor(locale, ns);
        // `with { type: 'json' }` is the standardized import attribute; bundlers also
        // accept a plain import of a .json module.
        importLines.push(`import ${ident} from '../locales/${locale}/${ns}.json' with {type: 'json'};`);
        entries.push(`    '${locale}/${ns}': ${ident}`);
    }
}

const banner = `/* eslint-disable */
// @generated by scripts/generate-locale-registry.js — DO NOT EDIT BY HAND.
// Static ESM registry of every bundled locale JSON, keyed by '<locale>/<namespace>'.
`;

const body = `${banner}
${importLines.join('\n')}

const REGISTRY = {
${entries.join(',\n')}
};

export default REGISTRY;

export function getResource(locale, ns) {
    return REGISTRY[locale + '/' + ns];
}
`;

fs.writeFileSync(OUT, body);
process.stdout.write(`Wrote ${OUT} (${importLines.length} locale files, ${locales.length} locales x ${NAMESPACES.length} namespaces)\n`);
