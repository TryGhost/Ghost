/* global process */
/**
 * Codegen — generates PER-NAMESPACE static ESM locale registries.
 *
 * The CJS build loads locales via a dynamic `require(`../locales/${locale}/${ns}.json`)`.
 * Bundlers using Rolldown can't resolve that (no `dynamicRequireTargets`), so browser
 * bundles silently drop locales. This script emits ONE module PER NAMESPACE, each with
 * STATIC `import`s of only that namespace's locale JSONs, keyed by locale code.
 *
 * Per-namespace (rather than one big registry) is deliberate: a public app that only
 * needs `portal` imports `@tryghost/i18n/registry/portal` and bundles ONLY the 62 portal
 * locale files — never the other 4 namespaces. This keeps each app's locale payload minimal.
 *
 * Outputs:
 *   ghost/i18n/lib/registry/<namespace>.generated.mjs   (one per namespace)
 *   ghost/i18n/lib/registry/index.generated.mjs         (lazy namespace router)
 */
const fs = require('fs');
const path = require('path');

const I18N_ROOT = path.join(__dirname, '..');
const LOCALES_DIR = path.join(I18N_ROOT, 'locales');
const LOCALE_DATA = require('../lib/locale-data.json');
const OUT_DIR = path.join(I18N_ROOT, 'lib', 'registry');

const NAMESPACES = ['ghost', 'portal', 'signup-form', 'comments', 'search'];

const locales = LOCALE_DATA.map(l => l.code);

function identFor(locale) {
    return `_${locale}`.replace(/[^a-zA-Z0-9_]/g, '_');
}

const banner = `/* eslint-disable */
// @generated by scripts/generate-locale-registry.js — DO NOT EDIT BY HAND.
`;

if (!fs.existsSync(OUT_DIR)) {
    fs.mkdirSync(OUT_DIR, {recursive: true});
}

let totalFiles = 0;

for (const ns of NAMESPACES) {
    const importLines = [];
    const entries = [];

    for (const locale of locales) {
        const abs = path.join(LOCALES_DIR, locale, `${ns}.json`);
        if (!fs.existsSync(abs)) {
            // Not every locale ships every namespace; only reference files that exist.
            continue;
        }
        const ident = identFor(locale);
        // `with {type: 'json'}` import attribute — REQUIRED by Node 22 ESM for JSON
        // modules, and accepted by Vite/Rollup, rolldown-vite and Vitest. Verified
        // against all four toolchains in the PR.
        importLines.push(`import ${ident} from '../../locales/${locale}/${ns}.json' with {type: 'json'};`);
        entries.push(`    '${locale}': ${ident}`);
        totalFiles += 1;
    }

    const body = `${banner}// Static ESM registry of all bundled '${ns}' locale JSONs, keyed by locale code.

${importLines.join('\n')}

const REGISTRY = {
${entries.join(',\n')}
};

export const namespace = '${ns}';

export default REGISTRY;

export function getResource(locale) {
    return REGISTRY[locale];
}
`;

    const out = path.join(OUT_DIR, `${ns}.generated.mjs`);
    fs.writeFileSync(out, body);

    // Per-namespace public entry (subpath export target). Apps import
    // `@tryghost/i18n/registry/<ns>` and get a factory bound to ONLY this
    // namespace's registry, so bundlers tree-shake the other namespaces away.
    const entryBody = `${banner}// Public entry for the '${ns}' namespace. Import as '@tryghost/i18n/registry/${ns}'.

import REGISTRY from './${ns}.generated.mjs';
import {createNamespacedI18n} from '../esm-factory.mjs';

const i18n = createNamespacedI18n(REGISTRY, '${ns}');

export default i18n;
export const {LOCALE_DATA, SUPPORTED_LOCALES, generateResources} = i18n;
`;
    fs.writeFileSync(path.join(OUT_DIR, `${ns}.mjs`), entryBody);
}

// A lazy router that maps a namespace string to its per-namespace registry via dynamic
// import(). Consumers that know their namespace at build time should import the specific
// per-namespace module instead (so bundlers tree-shake to a single namespace).
const routerCases = NAMESPACES
    .map(ns => `        case '${ns}': return (await import('./${ns}.generated.mjs')).default;`)
    .join('\n');

const routerBody = `${banner}// Lazy namespace router. Prefer importing './<namespace>.generated.mjs' directly
// when the namespace is known at build time — that lets bundlers drop the others.

export async function loadNamespaceRegistry(ns) {
    switch (ns) {
${routerCases}
        default: return {};
    }
}
`;

fs.writeFileSync(path.join(OUT_DIR, 'index.generated.mjs'), routerBody);

process.stdout.write(`Wrote ${OUT_DIR}/{${NAMESPACES.join(',')}}.generated.mjs + index (${totalFiles} locale files across ${NAMESPACES.length} namespaces, ${locales.length} locales)\n`);
